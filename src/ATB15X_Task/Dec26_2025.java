package ATB15X_Task;

public class Dec26_2025 {
    static void main(String[] args) {
        int a = 10;
        System.out.println(a++ + a++ + a++); // 10 + 11 + 12 = 33

        int x = 3;
        System.out.println(x++ * ++x); // 3 * 5 = 15

        int c = 5;
        System.out.println(c++ + c--); //5 + 6 = 11

    }
}
