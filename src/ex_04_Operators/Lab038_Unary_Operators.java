package ex_04_Operators;

public class Lab038_Unary_Operators {
    static void main(String[] args) {
//        Unary Operator
//        Apply to only 1 Operand
//        By default whn we assign a variable its +
        int a = +10;
        int a1 = -110;
        int result = a + a1;
        System.out.println(result);

        int b = -1;
        b = b+1;
        System.out.println(b);
    }
}
