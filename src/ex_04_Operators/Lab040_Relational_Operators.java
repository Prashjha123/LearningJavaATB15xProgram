package ex_04_Operators;

public class Lab040_Relational_Operators {
    static void main(String[] args) {
//        < Less Than
//        < = Less than or equal to
//        > Greater Than
//        > = Greater than or equal to
//        = = Equal to (but checking)
//        ! = Not equal to

        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);
    }
}
